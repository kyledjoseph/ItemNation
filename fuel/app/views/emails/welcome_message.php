<p>Thanks for signing up on ShopGab.com!!</p>

<p>We couldn't be more excited to have you as a user and to see how you can benefit from shopping socially.</p>

<p>To make sure you get the most out of ShopGab here are the simple steps to get going:</p>

<p>1) 'What are you shopping for?' - After logging in at shopgab.com click "Start a new quest" and fill out the basic information fields.</p>

<p>2) 'Add products you're looking at' - Click on the "+ Add Product" button, add the 'Add to ShopGab' button to your web-browser bookmark bar.  Then surf the web and click the button when viewing any product you'd like to add to your ShopGab quest.  For more info check out the following video: <a href="http://youtu.be/NzKArZOkmEk">http://youtu.be/NzKArZOkmEk</a></p>

<p>3) 'Invite your friends' - Click 'Message Friends' to send a private direct message through Facebook to friends who you would like to join your shopping quest.</p>

<p>4) 'Help your friends' - View your friends on the 'Friends' tab and see what they're shopping for; add comments, like/dislike products, or using the same 'Add to ShopGab' button, add products to your friend's quests.</p>

<p>Gather trusted feedback and recommended products to help you buy with confidence!</p>

